n=int(input("Ingrese número del 0 al 9= "))
if n==0:
    print("Cero")
elif n==1:
    print("Uno")
elif n==2:
    print("Dos")
elif n==3:
    print("Tres")
elif n==4:
    print("Cuatro")
elif n==5:
    print("Cinco")
elif n==6:
    print("Seis")
elif n==7:
    print("Siete")
elif n==8:
    print("Ocho")
elif n==9:
    print("Nueve")
else:
    print("Ingrese un número del 0 al 9")